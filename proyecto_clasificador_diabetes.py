"""## Bloque 2 — Arquitectura y Motor de Entrenamiento"""

import torch.nn as nn
import torch.optim as optim
import copy
from sklearn.metrics import (
    accuracy_score, precision_score, recall_score,
    f1_score, confusion_matrix, roc_auc_score, roc_curve
)

# ── Arquitectura dinámica ─────────────────────────────────────────────────
# neuronas_lista es una LISTA con el número de neuronas de cada capa oculta.
# Ejemplo: [64, 32] crea dos capas ocultas de 64 y 32 neuronas respectivamente.
# clase_activacion es la CLASE (no instancia) para crear una copia nueva por capa.
class RedDiabetes(nn.Module):
    def __init__(self, num_features, neuronas_lista, clase_activacion):
        super(RedDiabetes, self).__init__()

        capas = []
        entrada = num_features

        for neuronas in neuronas_lista:
            capas.append(nn.Linear(entrada, neuronas))
            capas.append(clase_activacion())  # nueva instancia por capa
            entrada = neuronas

        # Capa de salida: 1 neurona (clasificación binaria)
        # BCEWithLogitsLoss espera logits, NO probabilidades, así que NO se aplica Sigmoid aquí
        capas.append(nn.Linear(entrada, 1))

        self.red = nn.Sequential(*capas)

    def forward(self, x):
        return self.red(x)


# ── Función de entrenamiento con mini-batches y early stopping ────────────
def entrenar_modelo(config, train_loader, val_loader, num_features,
                    epocas=500, paciencia=20):
    """
    Entrena la red y devuelve:
      - El modelo con los MEJORES pesos (según val loss)
      - Historial de pérdida de entrenamiento y validación
      - Mejor val loss alcanzado
      - Época en que se detuvo
    """
    modelo = RedDiabetes(
        num_features=num_features,
        neuronas_lista=config['neuronas'],
        clase_activacion=config['act']
    )

    # BCEWithLogitsLoss = Sigmoid + BCELoss combinadas; más estable numéricamente
    criterio = nn.BCEWithLogitsLoss()

    # weight_decay implementa la regularización L2 (penaliza pesos grandes)
    optimizador = optim.Adam(
        modelo.parameters(), lr=config['lr'], weight_decay=config['l2']
    )

    hist_train, hist_val = [], []
    mejor_val_loss = float('inf')
    contador_paciencia = 0
    mejor_pesos = None
    epoca_parada = epocas

    for ep in range(epocas):
        # -- ENTRENAMIENTO (mini-batches) --
        modelo.train()
        loss_train_acum = 0.0
        for X_batch, y_batch in train_loader:
            optimizador.zero_grad()
            salida = modelo(X_batch)
            loss   = criterio(salida, y_batch)
            loss.backward()
            optimizador.step()
            loss_train_acum += loss.item() * X_batch.size(0)

        loss_train_ep = loss_train_acum / len(train_loader.dataset)

        # -- VALIDACIÓN --
        modelo.eval()
        loss_val_acum = 0.0
        with torch.no_grad():
            for X_batch, y_batch in val_loader:
                salida = modelo(X_batch)
                loss   = criterio(salida, y_batch)
                loss_val_acum += loss.item() * X_batch.size(0)

        loss_val_ep = loss_val_acum / len(val_loader.dataset)

        hist_train.append(loss_train_ep)
        hist_val.append(loss_val_ep)

        # -- EARLY STOPPING --
        if loss_val_ep < mejor_val_loss:
            mejor_val_loss = loss_val_ep
            contador_paciencia = 0
            # deepcopy para guardar una copia real de los pesos, no una referencia
            mejor_pesos = copy.deepcopy(modelo.state_dict())
        else:
            contador_paciencia += 1

        if contador_paciencia >= paciencia:
            epoca_parada = ep + 1
            break

    # Cargamos los mejores pesos (no los últimos, que pueden tener sobreajuste)
    modelo.load_state_dict(mejor_pesos)
    return modelo, hist_train, hist_val, mejor_val_loss, epoca_parada


# ── Función de evaluación completa ────────────────────────────────────────
def evaluar_modelo(modelo, X_test_t, y_test_t):
    """Devuelve todas las métricas del PDF para un modelo dado."""
    modelo.eval()
    with torch.no_grad():
        logits = modelo(X_test_t)
        # Sigmoid para convertir logits → probabilidades [0, 1]
        probs = torch.sigmoid(logits).numpy()

    preds = (probs >= 0.5).astype(int)
    y_np  = y_test_t.numpy()

    tn, fp, fn, tp = confusion_matrix(y_np, preds).ravel()

    acc  = accuracy_score(y_np, preds)
    prec = precision_score(y_np, preds, zero_division=0)
    rec  = recall_score(y_np, preds, zero_division=0)   # = Sensibilidad
    f1   = f1_score(y_np, preds, zero_division=0)
    esp  = tn / (tn + fp) if (tn + fp) > 0 else 0       # Especificidad manual
    auc  = roc_auc_score(y_np, probs)
    fpr, tpr, _ = roc_curve(y_np, probs)
    cm   = np.array([[tn, fp], [fn, tp]])

    return {
        'acc': acc, 'prec': prec, 'rec': rec, 'f1': f1,
        'esp': esp, 'auc': auc, 'fpr': fpr, 'tpr': tpr, 'cm': cm
    }

