"""## Bloque 3 — Entrenamiento de los 10 Modelos (A–J del PDF)"""

import time

NUM_FEATURES = X_train_norm_t.shape[1]

# ── Configuraciones exactas del PDF (tabla de la página 3) ───────────────
# 'neuronas' es una lista: cada elemento = neuronas de esa capa oculta
# 'act' es la CLASE de activación (sin paréntesis) para instanciar una copia por capa
configuraciones = {
    # --- Sin normalización ---
    'A': {'norm': False, 'neuronas': [16],           'act': nn.ReLU,      'lr': 0.01,    'l2': 0.0},
    'B': {'norm': False, 'neuronas': [32, 16],        'act': nn.ReLU,      'lr': 0.001,   'l2': 0.0},
    'C': {'norm': False, 'neuronas': [64, 32],        'act': nn.Tanh,      'lr': 0.001,   'l2': 0.0},
    'D': {'norm': False, 'neuronas': [128, 64, 32],   'act': nn.ReLU,      'lr': 0.0001,  'l2': 0.0},
    # --- Con normalización ---
    'E': {'norm': True,  'neuronas': [16],            'act': nn.ReLU,      'lr': 0.01,    'l2': 0.0},
    'F': {'norm': True,  'neuronas': [32, 16],        'act': nn.ReLU,      'lr': 0.001,   'l2': 0.0},
    'G': {'norm': True,  'neuronas': [64, 32],        'act': nn.LeakyReLU, 'lr': 0.001,   'l2': 0.0},
    'H': {'norm': True,  'neuronas': [128, 64, 32],   'act': nn.ELU,       'lr': 0.0001,  'l2': 0.0},
    'I': {'norm': True,  'neuronas': [64, 32, 16, 8], 'act': nn.ReLU,      'lr': 0.0005,  'l2': 0.0},
    'J': {'norm': True,  'neuronas': [32],            'act': nn.Tanh,      'lr': 0.01,    'l2': 0.0},
}

resultados = {}
t0 = time.time()

print('Iniciando entrenamiento de los 10 modelos...\n')

for nombre, config in configuraciones.items():
    print(f'Entrenando Modelo {nombre}  '
          f'(norm={config["norm"]}, capas={len(config["neuronas"])}, '
          f'neuronas={config["neuronas"]}, lr={config["lr"]})...')

    # Elegir loaders y tensor de prueba según escenario
    if config['norm']:
        tl, vl    = train_loader_norm, val_loader_norm
        X_test_ev = X_test_norm_t
    else:
        tl, vl    = train_loader_raw, val_loader_raw
        X_test_ev = X_test_raw_t

    modelo, h_train, h_val, mejor_val, ep_parada = entrenar_modelo(
        config=config,
        train_loader=tl,
        val_loader=vl,
        num_features=NUM_FEATURES,
        epocas=500,
        paciencia=20
    )

    metricas = evaluar_modelo(modelo, X_test_ev, y_test_t)

    resultados[nombre] = {
        'modelo':     modelo,
        'hist_train': h_train,
        'hist_val':   h_val,
        'mejor_val':  mejor_val,
        'epocas':     ep_parada,
        'metricas':   metricas,
        'config':     config
    }
    print(f'  -> Paró en época {ep_parada} | Val Loss: {mejor_val:.4f} | '
          f'Acc: {metricas["acc"]:.4f} | AUC: {metricas["auc"]:.4f}\n')

print(f'Entrenamiento completado en {time.time()-t0:.1f} segundos.')

